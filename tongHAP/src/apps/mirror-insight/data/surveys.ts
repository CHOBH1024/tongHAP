      advice: '비전을 현실로 만드는 탁월한 추진력을 가졌습니다. 여기에 Greenleaf가 강조한 \'경청(Listening)\'과 \'공감(Empathy)\'의 서번트 요소를 의식적으로 더해보십시오. 속도보다 함께 가는 방향을 점검할 때 공동체는 더 건강해집니다.'
