      description: '목회 리더십의 두 핵심 축(변혁, 서번트)보다는 기존의 시스템을 유지하고 관리하는 거래적 리더십(Transactional Leadership)에 가까운 상태입니다. 조직의 규칙, 전통, 안정성을 지키는 데는 뛰어나지만, 구성원의 잠재력을 끌어올리거나 깊은 정서적 유대를 형성하는 데는 에너지를 덜 쓰고 있을 수 있습니다.',
